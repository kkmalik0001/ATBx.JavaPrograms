package sept.ex_16092024_operator;

public class Lab0031_arithmeti_operator {
    public static void main(String[] args) {
        //Arithmetic operator are +,-,/,*
        // Binary in nature, required 2 operand to work with

        int a =10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        // % -->> modulus operator  -->> it gives remainder
        System.out.println(a%b);
        System.out.println(63%8);


    }
}
