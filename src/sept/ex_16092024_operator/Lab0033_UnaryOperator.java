package sept.ex_16092024_operator;

public class Lab0033_UnaryOperator {
    public static void main(String[] args) {
        // Unary Operator
        int a = +65;  // + ->> unary operator , there is no need to add single +, it is by default
        int b = -1;  // - ->> unary operator , - will have to be write
        System.out.println(a);
        System.out.println(b);

        b = b+1;
        System.out.println(b);
    }
}
