package sept.ex_20092024_ternaryOP_conditionANDloop;

public class Lab0065 {
    public static void main(String[] args) {
        // Max, min between 2 numbers by using ternary operation

        int tushar = 4;
        int prabhu = 7;

        //max between these two
        int max = tushar > prabhu ? tushar : prabhu;
        System.out.println(max);

        int min = tushar < prabhu ? tushar : prabhu;
        System.out.println(min);

    }
}
