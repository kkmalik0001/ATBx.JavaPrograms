package sept.ex_25092024_loops;

public class lab110 {
    public static void main(String[] args) {

        // similarly in while loop also
        // while also have 3 part- A (initialization),B(condition),C(updation)

        //WHILE SYNTAX
        int i =0;    //A
        while (i<=10){   //B
            System.out.println(i);
            i++;   //C
        }

        //FOR SYNTAX
        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }

    }
}
